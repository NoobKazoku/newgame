namespace GFrameworkGodotTemplate.scripts.ui.component;

/// <summary>
///     通用选项浏览器支持的编辑器类型。
/// </summary>
public enum OptionBrowserEditorKind
{
    /// <summary>
    ///     不显示可编辑控件。
    /// </summary>
    None,

    /// <summary>
    ///     使用整数输入控件。
    /// </summary>
    Integer,

    /// <summary>
    ///     使用离散选项控件。
    /// </summary>
    Choice
}